def translate_color(value: str) -> str | None:
    """Переводит японское название цвета в русское по ключевым словам."""
    if not value or value == "－":
        return None
    for jp_keyword, ru_color in COLOR_PATTERNS:
        if jp_keyword in value:
            return ru_color
    # Если ни один паттерн не подошёл — убираем японское
    import re
    cleaned = re.sub(r'[\u3000-\u9FFF\uFF00-\uFFEF（）「」【】・]+', ' ', value).strip()
    return cleaned if cleaned else None


